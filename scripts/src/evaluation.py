# Load packages
from sklearn.metrics import (
    confusion_matrix,
    classification_report,
    roc_auc_score,
    average_precision_score,
    fbeta_score,
    roc_curve
)

def evaluate_model(
    X_train,
    y_train,
    X_test,
    y_test,
    model,
    method
):

  """
  Perform model evaluation on a fitted model.

  Returns performance metrics, classification report, ROC curve
  coordinates, and confusion matrix for downstream analysis..
  """

  # Generate class predictions and predicted probabilities
  y_pred = model.predict(X_test)
  y_proba = model.predict_proba(X_test)[:, 1]

  # Create confusion matrix for model evaluation
  cm = confusion_matrix(y_test, y_pred)

  # Compute ROC curve coordinates for plotting
  (fpr,
   tpr,
   thresholds) = roc_curve(y_test, y_proba)

  # Classification report includes precision, recall,
  # F1-score, and support for each class
  report = classification_report(y_test, y_pred, zero_division=0)

  print(report)

  # Summary metrics used for model comparison
  metrics = {
      'Method': method,
      'Train Accuracy': model.score(X_train, y_train),
      'Test Accuracy': model.score(X_test, y_test),
      'F2 Score': fbeta_score(y_test, y_pred, beta=2), # Places greater emphasis  on recall than precision
      'ROC-AUC': roc_auc_score(y_test, y_proba),
      'Average Precision': average_precision_score(y_test, y_proba)}

  # Display summary metrics
  print(metrics)
  return {
      'metrics': metrics,
      'report': report,
      'fpr': fpr,
      'tpr': tpr,
      'thresholds': thresholds,
      'cm': cm,
      'y_pred': y_pred,
      'y_proba': y_proba
  }